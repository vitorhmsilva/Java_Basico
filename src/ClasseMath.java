public class ClasseMath {

	public static void main(String args[]) {
		
		//Métodos classe Math
		double x = 4.5, y = 0.0;
		System.out.println(Math.abs(x)); //valor absoluto
		System.out.println(Math.ceil(x)); //Arredonda pro menor inteiro, não menor que x
		System.out.println(Math.cos(y)); //cosseno trigonométrico do valor(em radianos)
		System.out.println(Math.floor(x)); //Arredonda pro maior inteiro, não maior que x
		System.out.println(Math.exp(x)); //método exponencial "e" elevado a x
		System.out.println(Math.log(x)); //logaritmo natural de x na base "e"
		System.out.println(Math.max(x, y)); //maior valor entre os x e y
		System.out.println(Math.min(x, y)); //menor valor entre os x e y
		System.out.println(Math.pow(x, y)); //potenciação x elevado a y
		System.out.println(Math.sin(y)); //seno trigonométrico do valor(em radianos)
		System.out.println(Math.tan(y)); //tangente trigonométrico do valor(em radianos)
		System.out.println(Math.sqrt(x)); //raiz quadrada de x
		System.out.println(Math.E); //base para logaritmos naturais (2.718...)
		System.out.println(Math.PI); //relação entre a circuferência e o diâmetro de um circulo qualquer (3.1415...)

	}
}
